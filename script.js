function getInitialLanguage() {
  const params = new URLSearchParams(window.location.search);
  const urlLang = params.get("lang");
  if (urlLang && translations[urlLang]) {
    return urlLang;
  }

  const storedLang = window.localStorage.getItem("trapani_lang");
  if (storedLang && translations[storedLang]) {
    return storedLang;
  }

  const browserLang = (navigator.language || "fr").slice(0, 2).toLowerCase();
  return translations[browserLang] ? browserLang : "fr";
}

function setLanguage(lang, options = { updateUrl: true }) {
  const selected = translations[lang] ? lang : "fr";
  const dict = translations[selected];
  activeLang = selected;

  document.documentElement.lang = selected;
  document.body.classList.remove("lang-fr", "lang-en", "lang-ar");
  document.body.classList.add(`lang-${selected}`);
  document.documentElement.dir = selected === "ar" ? "rtl" : "ltr";

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (dict[key]) {
      node.textContent = dict[key];
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((node) => {
    const key = node.dataset.i18nHtml;
    if (dict[key]) {
      node.innerHTML = dict[key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    if (dict[key]) {
      node.setAttribute("placeholder", dict[key]);
    }
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((node) => {
    const key = node.dataset.i18nAriaLabel;
    if (dict[key]) {
      node.setAttribute("aria-label", dict[key]);
    }
  });

  updateMetaTags(dict);
  updateOpenStatus(dict);
  updateLangButtons(selected);
  syncSeoLinks(selected, options.updateUrl);
  window.localStorage.setItem("trapani_lang", selected);

  window.requestAnimationFrame(() => {
    updateHeaderOffset();
    updateNavToggleLabel();
    applyArabicMobileFix();
    refreshMenuFiltering();
  });
}

function updateMetaTags(dict) {
  document.title = dict.metaTitle;
  const descriptionNode = document.getElementById("metaDescription");
  const ogTitleNode = document.getElementById("ogTitle");
  const ogDescriptionNode = document.getElementById("ogDescription");
  const twitterTitleNode = document.getElementById("twitterTitle");
  const twitterDescriptionNode = document.getElementById("twitterDescription");

  if (descriptionNode) descriptionNode.content = dict.metaDescription;
  if (ogTitleNode) ogTitleNode.content = dict.metaTitle;
  if (ogDescriptionNode) ogDescriptionNode.content = dict.metaDescription;
  if (twitterTitleNode) twitterTitleNode.content = dict.metaTitle;
  if (twitterDescriptionNode) twitterDescriptionNode.content = dict.metaDescription;
}

function updateLangButtons(lang) {
  document.querySelectorAll(".lang-btn").forEach((button) => {
    const isActive = button.dataset.lang === lang;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
}

function syncSeoLinks(lang, updateUrl) {
  const current = new URL(window.location.href);
  const hashValue = current.hash;
  current.hash = "";
  current.searchParams.delete("lang");

  const canonical = document.getElementById("canonicalLink");
  if (canonical) {
    canonical.href = current.toString();
  }

  document.querySelectorAll('link[rel="alternate"][hreflang]').forEach((link) => {
    const code = link.getAttribute("hreflang");
    const nextUrl = new URL(current.toString());
    if (translations[code]) {
      nextUrl.searchParams.set("lang", code);
      link.href = nextUrl.toString();
    } else if (code === "x-default") {
      nextUrl.searchParams.set("lang", "fr");
      link.href = nextUrl.toString();
    }
  });

  const ogUrl = document.querySelector('meta[property="og:url"]');
  if (ogUrl) {
    const ogLink = new URL(current.toString());
    ogLink.searchParams.set("lang", lang);
    ogUrl.content = ogLink.toString();
  }

  if (updateUrl && window.history.replaceState) {
    const next = new URL(current.toString());
    next.searchParams.set("lang", lang);
    next.hash = hashValue;
    window.history.replaceState({}, "", `${next.pathname}${next.search}${next.hash}`);
  }
}

function getCasablancaTimeParts() {
  const formatter = new Intl.DateTimeFormat("en-GB", {
    weekday: "short",
    hour: "2-digit",
    minute: "2-digit",
    hourCycle: "h23",
    timeZone: "Africa/Casablanca"
  });

  const parts = formatter.formatToParts(new Date());
  const weekday = parts.find((part) => part.type === "weekday")?.value || "Mon";
  const hour = Number(parts.find((part) => part.type === "hour")?.value || 0);
  const minute = Number(parts.find((part) => part.type === "minute")?.value || 0);

  const dayMap = {
    Sun: 0,
    Mon: 1,
    Tue: 2,
    Wed: 3,
    Thu: 4,
    Fri: 5,
    Sat: 6
  };

  return {
    dayIndex: dayMap[weekday] ?? 1,
    currentMinutes: hour * 60 + minute
  };
}

function formatTime(totalMinutes) {
  const hours = String(Math.floor(totalMinutes / 60)).padStart(2, "0");
  const minutes = String(totalMinutes % 60).padStart(2, "0");
  return `${hours}:${minutes}`;
}

function updateOpenStatus(dict) {
  const statusNode = document.getElementById("openStatus");
  const detailNode = document.getElementById("todayStatusLine");
  if (!statusNode || !detailNode) {
    return;
  }

  const { dayIndex, currentMinutes } = getCasablancaTimeParts();
  const slot = scheduleByDay[dayIndex];
  const dayLabel = dict[orderedDayKeys[dayIndex]];
  let isOpen = false;
  let detail = "";

  if (slot.fullDay) {
    isOpen = true;
    detail = `${dayLabel}: ${dict.hours24}`;
  } else {
    isOpen = currentMinutes >= slot.open && currentMinutes < slot.close;
    const template = isOpen ? dict.todayOpenUntil : dict.todayOpensAt;
    const time = isOpen ? formatTime(slot.close) : formatTime(slot.open);
    detail = `${dayLabel}: ${template.replace("{time}", time)}`;
  }

  statusNode.textContent = isOpen ? dict.statusOpen : dict.statusClosed;
  statusNode.classList.toggle("is-open", isOpen);
  statusNode.classList.toggle("is-closed", !isOpen);
  detailNode.textContent = detail;
}

function updateHeaderOffset() {
  const header = document.querySelector(".site-header");
  if (!header) {
    return;
  }

  const offset = Math.ceil(header.getBoundingClientRect().height + 22);
  document.documentElement.style.setProperty("--header-offset", `${offset}px`);
}

function updateNavToggleLabel() {
  const header = document.querySelector(".site-header");
  const toggle = document.querySelector(".nav-toggle");
  if (!header || !toggle) {
    return;
  }

  const dict = translations[activeLang] || translations.fr;
  const isOpen = header.classList.contains("nav-open");
  const label = isOpen ? dict.navToggleCloseLabel : dict.navToggleOpenLabel;
  toggle.setAttribute("aria-label", label);
}

function setMobileNavState(isOpen) {
  const header = document.querySelector(".site-header");
  const toggle = document.querySelector(".nav-toggle");
  if (!header || !toggle) {
    return;
  }

  header.classList.toggle("nav-open", isOpen);
  toggle.setAttribute("aria-expanded", String(isOpen));
  updateNavToggleLabel();
  updateHeaderOffset();
}

function normalizeText(value) {
  return String(value || "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase();
}

function refreshMenuFiltering() {
  const menuSection = document.getElementById("menu");
  if (!menuSection) {
    return;
  }

  const dict = translations[activeLang] || translations.fr;
  const normalizedQuery = normalizeText(menuSearchQuery.trim());
  let visibleItemsCount = 0;

  menuSection.querySelectorAll(".card").forEach((card) => {
    let cardVisibleCount = 0;

    card.querySelectorAll(".menu-item").forEach((item) => {
      const tags = (item.dataset.tags || "")
        .split(",")
        .map((tag) => tag.trim())
        .filter(Boolean);
      const itemText = normalizeText(item.textContent);
      const matchesQuery = !normalizedQuery || itemText.includes(normalizedQuery);
      const matchesFilter = activeMenuFilter === "all" || tags.includes(activeMenuFilter);
      const isVisible = matchesQuery && matchesFilter;

      item.hidden = !isVisible;
      if (isVisible) {
        cardVisibleCount += 1;
        visibleItemsCount += 1;
      }
    });

    card.hidden = cardVisibleCount === 0;
  });

  const summaryNode = document.getElementById("menuSearchSummary");
  if (summaryNode) {
    if (normalizedQuery || activeMenuFilter !== "all") {
      summaryNode.textContent = dict.menuResultsCount.replace("{count}", String(visibleItemsCount));
    } else {
      summaryNode.textContent = dict.menuSearchHint;
    }
  }

  const noResultsNode = document.getElementById("menuNoResults");
  if (noResultsNode) {
    noResultsNode.hidden = visibleItemsCount > 0;
  }
}

function initMenuFilters() {
  const searchInput = document.getElementById("menuSearchInput");
  if (searchInput) {
    searchInput.addEventListener("input", (event) => {
      menuSearchQuery = event.target.value || "";
      refreshMenuFiltering();
    });
  }

  document.querySelectorAll(".filter-btn").forEach((button) => {
    button.addEventListener("click", () => {
      activeMenuFilter = button.dataset.filter || "all";

      document.querySelectorAll(".filter-btn").forEach((node) => {
        const isActive = node === button;
        node.classList.toggle("active", isActive);
        node.setAttribute("aria-pressed", String(isActive));
      });

      refreshMenuFiltering();
    });
  });

  refreshMenuFiltering();
}

function applyArabicMobileFix() {
  const root = document.documentElement;
  if (activeLang !== "ar" || window.innerWidth > 760) {
    root.style.fontSize = "";
    return;
  }

  let size = 100;
  root.style.fontSize = "100%";
  let guard = 0;

  while (root.scrollWidth > window.innerWidth + 2 && size > 88 && guard < 8) {
    size -= 2;
    root.style.fontSize = `${size}%`;
    guard += 1;
  }
}

function initMobileNav() {
  const toggle = document.querySelector(".nav-toggle");
  const header = document.querySelector(".site-header");
  if (!toggle || !header) {
    return;
  }

  toggle.addEventListener("click", () => {
    const nextState = !header.classList.contains("nav-open");
    setMobileNavState(nextState);
  });

  header.querySelectorAll(".main-nav a").forEach((link) => {
    link.addEventListener("click", () => setMobileNavState(false));
  });

  document.addEventListener("click", (event) => {
    if (window.innerWidth > 760) {
      return;
    }
    if (!header.contains(event.target)) {
      setMobileNavState(false);
    }
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
      setMobileNavState(false);
    }
  });

  updateNavToggleLabel();
}

function initLanguageSwitch() {
  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.addEventListener("click", () => {
      setLanguage(button.dataset.lang, { updateUrl: true });
    });
  });
}

let resizeTimer = null;
window.addEventListener("resize", () => {
  window.clearTimeout(resizeTimer);
  resizeTimer = window.setTimeout(() => {
    if (window.innerWidth > 760) {
      setMobileNavState(false);
    }
    updateHeaderOffset();
    applyArabicMobileFix();
    updateOpenStatus(translations[activeLang]);
  }, 120);
});

window.addEventListener("orientationchange", () => {
  window.setTimeout(() => {
    updateHeaderOffset();
    applyArabicMobileFix();
  }, 150);
});

window.addEventListener("DOMContentLoaded", () => {
  initMobileNav();
  initLanguageSwitch();
  initMenuFilters();
  updateHeaderOffset();
  setLanguage(getInitialLanguage(), { updateUrl: false });
  window.setInterval(() => updateOpenStatus(translations[activeLang]), 60000);
});
